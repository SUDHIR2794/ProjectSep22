package org.abs;

public abstract class Student {

	abstract void name();
	abstract void age();
	abstract void height();
	abstract void bloodgroup();
	abstract void mobile();


	public void bike() {
		System.out.println("My bike is apache 160RTR");
	}
	public void car() {
		System.out.println("My car is KIA Seltos");

	}
	public void cycle() {
		System.out.println("My cycle is Hercules");
	}
		
}
	
